import os
import sys
from pathlib import Path
import getpass

def create_secure_env():
    """Create .env file securely"""
    print("ğŸ”’ Secure DJ AI System Setup")
    print("=" * 40)

    env_file = Path(".env")

    if env_file.exists():
        response = input("âš ï¸  .env file already exists. Overwrite? (y/N): ")
        if response.lower() != 'y':
            print("Setup cancelled.")
            return False

    print("\nğŸ”‘ Please enter your OpenRouter API key:")
    print("   (Get yours at: https://openrouter.ai)")
    print("   (Input will be hidden for security)")

    api_key = getpass.getpass("OpenRouter API Key: ")

    if not api_key or not api_key.startswith('sk-or-v1-'):
        print("âŒ Invalid API key format. Must start with 'sk-or-v1-'")
        return False

    # Optional settings
    print("\nâš™ï¸  Optional settings (press Enter for defaults):")
    music_path = input(f"Music library path [{os.path.expanduser('~/Music')}]: ").strip()
    if not music_path:
        music_path = os.path.expanduser('~/Music')

    venue = input("Default venue [club]: ").strip() or "club"
    event = input("Default event [prime_time]: ").strip() or "prime_time"
    duration = input("Default duration [60]: ").strip() or "60"

    # Create .env content
    env_content = f"""# DJ AI System - Environment Variables
# NEVER commit this file to GitHub!

# OpenRouter API Key
OPENROUTER_API_KEY={api_key}

# Music Library Path
MUSIC_PATH={music_path}

# DJ AI Configuration
DJ_DEFAULT_VENUE={venue}
DJ_DEFAULT_EVENT={event}
DJ_DEFAULT_DURATION={duration}

# Generated by secure setup script
"""

    try:
        with open(env_file, 'w') as f:
            f.write(env_content)

        # Set file permissions (Unix/macOS only)
        if os.name == 'posix':
            os.chmod(env_file, 0o600)  # Read/write for owner only

        print("\nâœ… .env file created successfully!")
        print("ğŸ”’ File permissions set to owner-only access")
        print(f"ğŸ“ Created: {env_file.absolute()}")

        # Verify .gitignore
        gitignore = Path(".gitignore")
        if gitignore.exists():
            with open(gitignore, 'r') as f:
                content = f.read()
                if '.env' in content:
                    print("âœ… .env is already in .gitignore - protected from Git")
                else:
                    print("âš ï¸  Adding .env to .gitignore for security")
                    with open(gitignore, 'a') as f:
                        f.write("\n# Environment variables\n.env\n")

        return True

    except Exception as e:
        print(f"âŒ Error creating .env file: {e}")
        return False

